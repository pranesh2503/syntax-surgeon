def chat_handler(user_message, chat_history, temperature, max_tokens, top_p):
    if not user_message.strip():
        return chat_history, ""
    parts = []
    if detect_python_code(user_message):
        result = fix_python_code(user_message)
        if result['changes']:
            parts.append("**Code fixes applied:**")
            parts += [f"  - {c}" for c in result['changes']]
            parts.append("")
        parts.append("Syntax valid." if result['syntax_valid']
                     else f"Remaining issue: {result['error']}")
        parts.append("\n**Fixed code:**")
        parts.append(f"```python\n{result['fixed']}\n```")
        prompt = (f"Briefly explain what this fixed Python code does:\n\n"
                  f"```python\n{result['fixed']}\n```")
        parts.append("\n**Explanation:**")
        parts.append(generate_response(prompt, temperature, max_tokens, top_p))
    else:
        parts.append(generate_response(user_message, temperature,
                                       int(max_tokens), top_p))
    chat_history.append((user_message, '\n'.join(parts)))
    return chat_history, ""


def clear_chat():
    reset_history()
    return [], "Chat cleared."


