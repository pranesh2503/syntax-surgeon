def detect_python_code(text):
    patterns = [
        r'def\s+\w+\s*\(',
        r'class\s+\w+',
        r'import\s+\w+',
        r'from\s+\w+\s+import',
        r'if\s+.*:',
        r'for\s+.*\s+in\s+',
        r'while\s+.*:',
        r'```python',
        r'print\s*\(',
        r'return\s+',
    ]
    return any(re.search(p, text) for p in patterns)


# -- 2.2 Syntax Validation --
def check_syntax(code):
    try:
        ast.parse(code)
        return True, None
    except SyntaxError as e:
        return False, f"Syntax Error (line {e.lineno}): {e.msg}"
    except Exception as e:
        return False, str(e)


# -- 2.3 Code Fixing Engine --
def fix_code_indentation(code):
    lines = code.splitlines()
    return '\n'.join(line.rstrip().replace('\t', '    ') for line in lines)


def fix_code_brackets(code):
    pairs = {'(': ')', '[': ']', '{': '}'}
    stack = []
    for ch in code:
        if ch in pairs:
            stack.append(pairs[ch])
        elif ch in pairs.values():
            if stack and stack[-1] == ch:
                stack.pop()
    return code + ''.join(reversed(stack))


def format_code(code):
    try:
        import black
        return black.format_str(code, mode=black.Mode())
    except Exception:
        pass
    try:
        import autopep8
        return autopep8.fix_code(code, options={'aggressive': 1})
    except Exception:
        return code


def fix_python_code(code):
    code = re.sub(r'^```python\s*', '', code.strip(), flags=re.MULTILINE)
    code = re.sub(r'```$', '', code.strip(), flags=re.MULTILINE)
    code = code.strip()
    original, changes = code, []

    fixed = fix_code_indentation(code)
    if fixed != code:
        changes.append("Normalised indentation (tabs to spaces)")
    code = fixed

    fixed = fix_code_brackets(code)
    if fixed != code:
        changes.append("Balanced unmatched brackets/parentheses")
    code = fixed

    formatted = format_code(code)
    if formatted.strip() != code.strip():
        changes.append("Applied PEP 8 formatting (Black/autopep8)")
    code = formatted

    is_valid, error = check_syntax(code)
    return {'original': original, 'fixed': code,
            'syntax_valid': is_valid, 'error': error, 'changes': changes}


# -- 3.1 Conversation History --
conversation_history = []

def reset_history():
    global conversation_history
    conversation_history = []


# -- 3.2 Response Generation --
def generate_response(user_message, temperature, max_tokens, top_p):
    global conversation_history
    conversation_history.append({"role": "user", "content": user_message})
    try:
        output = model_pipe(
            conversation_history,
            max_new_tokens=int(max_tokens),
            temperature=temperature,
            do_sample=True,
            top_p=top_p,
        )
        reply = output[0]["generated_text"][-1]["content"]
    except Exception as e:
        reply = f"Model error: {e}"
    conversation_history.append({"role": "assistant", "content": reply})
    return reply


# -- 4.2 Export Utilities --
def download_txt():
    if not conversation_history:
        return None, "No conversation to export."
    lines = ["=== The Syntax Surgeon - Chat Export ===",
             f"Exported : {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
             f"Messages : {len(conversation_history)}",
             "=" * 50, ""]
    for msg in conversation_history:
        lines += [f"[{msg['role'].upper()}]", msg['content'], ""]
    tmp = tempfile.NamedTemporaryFile(delete=False, suffix=".txt",
                                      mode="w", encoding="utf-8")
    tmp.write('\n'.join(lines))
    tmp.close()
    return tmp.name, "TXT export ready."


def download_pdf():
    if not conversation_history:
        return None, "No conversation to export."
    try:
        from reportlab.lib.pagesizes import letter
        from reportlab.lib import colors
        from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.units import inch

        tmp = tempfile.NamedTemporaryFile(delete=False, suffix=".pdf")
        doc = SimpleDocTemplate(tmp.name, pagesize=letter,
                                leftMargin=inch, rightMargin=inch,
                                topMargin=inch, bottomMargin=inch)
        styles = getSampleStyleSheet()
        user_style = ParagraphStyle('User', parent=styles['Normal'],
                                    textColor=colors.HexColor('#1a3c6e'),
                                    fontName='Helvetica-Bold', fontSize=10)
        asst_style = ParagraphStyle('Asst', parent=styles['Normal'],
                                    textColor=colors.HexColor('#4b0082'),
                                    fontName='Helvetica', fontSize=10)
        story = [
            Paragraph("The Syntax Surgeon - Chat Export", styles['Title']),
            Paragraph(
                f"Exported: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')} | "
                f"Messages: {len(conversation_history)}", styles['Normal']),
            Spacer(1, 0.2 * inch),
        ]
        for msg in conversation_history:
            role = msg['role'].upper()
            content = msg['content'].replace('<', '&lt;').replace('>', '&gt;')
            style = user_style if msg['role'] == 'user' else asst_style
            story.append(Paragraph(f"<b>[{role}]</b>", style))
            for line in content.split('\n'):
                story.append(Paragraph(line or "&nbsp;", styles['Normal']))
            story.append(Spacer(1, 0.15 * inch))
        doc.build(story)
        tmp.close()
        return tmp.name, "PDF export ready."
    except Exception as e:
        return None, f"PDF error: {e}"


# -- 4.3 Pastebin Share --
def generate_share_link():
    if not conversation_history:
        return "No conversation to share."
    lines = ["The Syntax Surgeon - Shared Chat",
             f"Shared: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
             "=" * 50]
    for msg in conversation_history:
        lines += [f"\n[{msg['role'].upper()}]", msg['content']]
    try:
        resp = requests.post(
            'https://pastebin.com/api/api_post.php',
            data={
                'api_dev_key':          'PASTE_YOUR_PASTEBIN_API_KEY_HERE',
                'api_option':           'paste',
                'api_paste_code':       '\n'.join(lines),
                'api_paste_name':       'Syntax Surgeon Chat',
                'api_paste_expire_date':'1W',
                'api_paste_private':    '0',
            }, timeout=10)
        if resp.status_code == 200 and resp.text.startswith('https'):
            return f"Share link: {resp.text.strip()}"
        return f"Pastebin response: {resp.text}"
    except Exception as e:
        return f"Share error: {e}"


